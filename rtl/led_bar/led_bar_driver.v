//============================================================
// 模块名称：led_bar_driver
// 功能说明：
//   根据超声波测得的距离点亮 AX301 板载 4 个 LED，形成类似倒车雷达的
//   距离条显示效果。
//
// 距离单位：
//   distance 输入沿用本工程内部统一格式，单位为 0.001cm。
//   例如：
//     100000 表示 100.000cm
//      75000 表示  75.000cm
//      25000 表示  25.000cm
//
// 显示规则：
//   distance = 0 或 distance > 100cm：认为无目标或目标较远，LED 全灭；
//   75cm < distance <= 100cm：点亮 1 个 LED；
//   50cm < distance <=  75cm：点亮 2 个 LED；
//   25cm < distance <=  50cm：点亮 3 个 LED；
//    0cm < distance <=  25cm：点亮 4 个 LED。
//
// 硬件极性：
//   AX301 板载 LED 为高电平点亮。
//   因此本模块输出 1 表示对应 LED 点亮，输出 0 表示熄灭。
//============================================================
module led_bar_driver(
    input  wire [18:0] distance, // 距离输入，单位 0.001cm
    output reg  [3:0]  led       // 板载 4 个 LED，高电平点亮
);

    //========================================================
    // 距离阈值定义
    // 使用 localparam 集中管理阈值，后续如果要调整报警距离，
    // 只需要修改这里，不需要改动下面的组合逻辑。
    //========================================================
    localparam [18:0] DIST_25CM  = 19'd25_000;
    localparam [18:0] DIST_50CM  = 19'd50_000;
    localparam [18:0] DIST_75CM  = 19'd75_000;
    localparam [18:0] DIST_100CM = 19'd100_000;

    //========================================================
    // LED 距离条组合逻辑
    // led[0] 表示最远一级，led[3] 表示最近一级；随着距离减小，
    // 输出从 0001 逐级扩展为 0011、0111、1111，形成“灯条”效果。
    // 每一档的下边界归入更近的一档，例如 distance == 75cm 时点亮 2 个 LED。
    // distance == 0 在这里按无效距离处理，避免刚上电或无回波时误触发满格报警。
    //========================================================
    always @(*) begin
        if ((distance == 19'd0) || (distance > DIST_100CM))
            led = 4'b0000;
        else if (distance > DIST_75CM)
            led = 4'b0001;
        else if (distance > DIST_50CM)
            led = 4'b0011;
        else if (distance > DIST_25CM)
            led = 4'b0111;
        else
            led = 4'b1111;
    end

endmodule
